#include <bits/stdc++.h>

using namespace std;

#define ll long long

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    ll t;
    cin >> t;
    while(t--)
    {
        ll n, m, i, j;
        cin >> n >> m >> i >> j;
        cout << 1 << " " << 1 << " " << n << " " << m << "\n";
    }

    return 0;
}